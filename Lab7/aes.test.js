const { aesDecrypt, aesEncrypt } = require("./aes.js");

const plaintext = [
  0x32, 0x88, 0x31, 0xe0, 0x43, 0x5a, 0x31, 0x37, 0xf6, 0x30, 0x98, 0x07, 0xa8,
  0x8d, 0xa2, 0x34,
];
const key = [
  0x2b, 0x7e, 0x15, 0x16, 0x28, 0xae, 0xd2, 0xa6, 0xab, 0xf7, 0x97, 0x75, 0x46,
  0x31, 0x76, 0x9b,
];

test("encrypted should be equal", () => {
  const ciphertext = aesEncrypt(plaintext, key);
  
  const ciphertextEqual = [
    0x14, 0x4d, 0x8f, 0xd9, 0x34, 0x03, 0x02, 0xe7, 0x26, 0x1f, 0x5d, 0xe0,
    0x35, 0xff, 0xc4, 0x63
  ];
  expect(ciphertext).toEqual(ciphertextEqual);
});

test("decrypted should be equal", () => {
    const ciphertext = aesEncrypt(plaintext, key);
    const decrypted = aesDecrypt(ciphertext, key);
    
    expect(decrypted).toEqual(plaintext);
  });
